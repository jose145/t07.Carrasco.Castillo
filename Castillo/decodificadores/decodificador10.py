import os

# Decodificar mensaje encriptado
msg="YSDLP"

for letra in msg:
    if letra == "Y":
        print(os.sys.argv[1])
    if letra == "S":
        print(os.sys.argv[2])
    if letra == "D":
        print(os.sys.argv[3])
    if letra == "L":
        print(os.sys.argv[4])
    if letra == "P":
        print(os.sys.argv[5])
#fin_iterador

print("Fin del bucle")

