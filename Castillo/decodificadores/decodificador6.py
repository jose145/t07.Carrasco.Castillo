import os

# Decodificar mensaje encriptado
msg="JSEOESJ"

for letra in msg:
    if letra == "J":
        print(os.sys.argv[1])
    if letra == "O":
        print(os.sys.argv[2])
    if letra == "S":
        print(os.sys.argv[3])
    if letra == "E":
        print(os.sys.argv[4])
#fin_iterador

print("Fin del bucle")

